import type { AnnouncementRow } from "./announcements";

const TODAY_KST = new Date(Date.now() + 9 * 3600 * 1000).toISOString().slice(0, 10);

interface Seed {
  title: string;
  org: string;
  cats: number[];
  region: string;
  target: string;
  support: string;
  summary: string;
  start: string | null;
  end: string | null; // null = 상시
  created: string; // YYYY-MM-DD
}

let seq = 0;
function row(s: Seed): AnnouncementRow {
  const id = ++seq;
  return {
    id,
    source_id: (id % 5) + 1,
    title: s.title,
    organization: s.org,
    category_ids: s.cats,
    region: s.region,
    target: s.target,
    support_type: s.support,
    summary: s.summary,
    apply_start: s.start,
    apply_end: s.end,
    detail_url: `https://www.bizinfo.go.kr/web/lay1/bbs/S1T122C128/AS/74/view.do?pblancId=PBLN_${String(
      id
    ).padStart(12, "0")}`,
    status: s.end && s.end < TODAY_KST ? "closed" : "open",
    created_at: `${s.created}T09:00:00+09:00`,
  };
}

